<?php

// Input form
$nama = 'Moch';
$tb='158'/100;
$bb='40';
 
/* Rumus BMI adalah:
BMI = BERAT BADAN / KUADRAT TINGGI BADAN
*/
// Hitung BMI
$bmi = $bb / ($tb* $tb);
// Mencetak hasil
echo '<div class="hasil">';
echo "<h3>Hasil perhitungan BMI</h3>"; 
echo "Nama Anda: $nama<br>";
echo "Tinggi Badan: $tb meter<br>";
echo "Berat Badan: $bb kilogram<br>"; 
echo "<hr>BMI Anda: ".number_format($bmi); 
echo "<h4>Kesimpulan: Hallo $nama , Nilai BMI Anda Adalah $bmi Anda Temasuk </h4>";
// Menghitung dan mencetak kesimpulan 
if($bmi < 17) {
echo "Kurus Banget";
}elseif ($bmi <= 18.5 ) {
echo "Kurus";
}elseif ($bmi <= 25 ) {
    echo "Normal";
}elseif ($bmi <= 27 ) {
echo "Gemuk";
}elseif ($bmi > 27 ) { 
    echo "Obesitas";
}
// closing div 
echo '</div>';

echo '====================================================================';

